    private void hitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hitungActionPerformed
        // TODO add your handling code here:
        if(Integer.parseInt(bayar.getText())<Integer.parseInt(totalsemua.getText())){
            JOptionPane.showMessageDialog(null, "Uang anda Tidak cukup untuk melakukan pembayaran, silahkan input kembali jumlah pembayaran");
        }
        int uangKembalian = Integer.parseInt(bayar.getText())-Integer.parseInt(totalsemua.getText());
        kembalian.setText(""+uangKembalian);
    }//GEN-LAST:event_hitungActionPerformed

